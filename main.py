def retangulo(x, y, largura, altura, cor):
    t.color(cor)
    t.penup()
    t.goto(x, y)
    t.setheading(90)  # garante que começa reto
    t.pendown()
    for _ in range(2):
        t.forward(altura)
        t.left(90)
        t.forward(largura)
        t.left(90)
